    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
        
        //delete row
        if(jTable1.getSelectedRowCount()== 1){
            //if single row is selected the delete
            tblModel.removeRow(jTable1.getSelectedRow());
        }else{
            if(jTable1.getRowCount() == 0){
                //if table is empty
                JOptionPane.showMessageDialog(this, "Table is empty");
            }else{
                //if table is not empty but row is not selected
                JOptionPane.showMessageDialog(this, "Please select one row to delete");
            }
        }
    }//GEN-LAST:event_deleteActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
        if(jTable1.getSelectedRowCount() == 1){
            //if single row is selected then update
            String id = empID.getText();
            String empName = name.getText();
            String empFaculty = faculty.getText();
            String empLevel = level.getText();
            
            
            //set updated value on table row
            tblModel.setValueAt(empID,jTable1.getSelectedRow(),0);
            tblModel.setValueAt(name,jTable1.getSelectedRow(),1);
            tblModel.setValueAt(faculty,jTable1.getSelectedRow(),2);
            tblModel.setValueAt(level,jTable1.getSelectedRow(),3);
            
            
            //update message display
            JOptionPane.showMessageDialog(this,"Successfully updated");
            
        }else{
            if(jTable1.getRowCount() == 0){
                JOptionPane.showMessageDialog(this,"Table is empty");
            }else{
                JOptionPane.showMessageDialog(this,"Please select one row to update");
            }
        }
    }//GEN-LAST:event_updateActionPerformed

