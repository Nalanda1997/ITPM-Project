    public void addLecturerTable(){
        try{
            
            DB_Connection obj_DB_Connection= new DB_Connection();
            Connection con = obj_DB_Connection.connect();
            
            PreparedStatement ps = con.prepareStatement("select id,name,contact,level,center,faculty,department,rank from lecturer");
            ResultSet rs = ps.executeQuery();
            ResultSetMetaData rsm = rs.getMetaData();
            int c;
            c = rsm.getColumnCount();
            DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            while(rs.next()){
                Vector v2 = new Vector();
                for(int i =1; i<=c; i++){
                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("name"));
                    v2.add(rs.getString("contact"));
                    v2.add(rs.getString("level"));
                    v2.add(rs.getString("center"));
                    v2.add(rs.getString("faculty"));
                    v2.add(rs.getString("department"));
                    v2.add(rs.getString("rank"));  
                }
                df.addRow(v2);
            }
        }catch(Exception e){
            System.out.println(e);
        }
    }
    
