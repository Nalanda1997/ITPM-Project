    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        
        DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
        if(jTable1.getSelectedRowCount() == 1){
            //if single row is selected then update
            String module_code = subjectCode.getText();
            String module_name = subjectName.getText();
            String offered_year = offeredYear.getText();
            String offered_sem = offeredSem.getText();
            String credit_points = creditPoints.getText();
            
            //set updated value on table row
            tblModel.setValueAt(module_code,jTable1.getSelectedRow(),0);
            tblModel.setValueAt(module_name,jTable1.getSelectedRow(),1);
            tblModel.setValueAt(offered_year,jTable1.getSelectedRow(),2);
            tblModel.setValueAt(offered_sem,jTable1.getSelectedRow(),3);
            tblModel.setValueAt(credit_points,jTable1.getSelectedRow(),4);
            
            //update message display
            JOptionPane.showMessageDialog(this,"Successfully updated");
            
        }else{
            if(jTable1.getRowCount() == 0){
                JOptionPane.showMessageDialog(this,"Table is empty");
            }else{
                JOptionPane.showMessageDialog(this,"Please select one row to update");
            }
        }
    }//GEN-LAST:event_updateActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        //get jtable model
        DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
        
        //delete row
        if(jTable1.getSelectedRowCount()== 1){
            //if single row is selected the delete
            tblModel.removeRow(jTable1.getSelectedRow());
        }else{
            if(jTable1.getRowCount() == 0){
                //if table is empty
                JOptionPane.showMessageDialog(this, "Table is empty");
            }else{
                //if table is not empty but row is not selected
                JOptionPane.showMessageDialog(this, "Please select one row to delete");
            }
        }
    }//GEN-LAST:event_deleteActionPerformed
