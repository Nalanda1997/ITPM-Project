    public void addSubjectTable(){
        try{
            
            DB_Connection obj_DB_Connection= new DB_Connection();
            Connection con = obj_DB_Connection.connect();
    
            PreparedStatement ps = con.prepareStatement("select * from subject");
            ResultSet rs = ps.executeQuery();
            ResultSetMetaData rsm = rs.getMetaData();
            int c;
            c = rsm.getColumnCount();
            DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            while(rs.next()){
                Vector v2 = new Vector();
                for(int i =1; i<=c; i++){
                    v2.add(rs.getString("module_code"));
                    v2.add(rs.getString("module_name"));
                    v2.add(rs.getString("offered_year"));
                    v2.add(rs.getString("offered_sem"));
                    v2.add(rs.getString("no_of_lecture_hrs"));
                    v2.add(rs.getString("no_of_lab_hrs"));
                    v2.add(rs.getString("no_of_tute_hrs"));
                    v2.add(rs.getString("evaluation_criteria"));
                    v2.add(rs.getString("credit_points"));
                    v2.add(rs.getString("gpa_status"));
                    v2.add(rs.getString("credit_status"));
                   
                }
                df.addRow(v2);
            }
        }catch(Exception e){
            System.out.println(e);
        }
    }


